  AUTH_PROVIDER: "${AUTH_PROVIDER:-cognito}",
  COGNITO_POOL_ID: "${COGNITO_POOL_ID:-}",
  COGNITO_CLIENT_ID: "${COGNITO_CLIENT_ID:-}",
  COGNITO_DOMAIN: "${COGNITO_DOMAIN:-}",
